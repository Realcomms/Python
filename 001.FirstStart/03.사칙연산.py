# 사칙연산


def plus(val1, val2):
    return val1 + val2


def sub(val1, val2):
    return val1 - val2


def mul(val1, val2):
    return val1 * val2


def div(val1, val2):
    return val1 / val2


result_plus = plus(10,20)
print(result_plus)
result_plus2 = plus(20,30)
print(result_plus2)
result_plus3 = plus(result_plus, result_plus2)
print(result_plus3)
